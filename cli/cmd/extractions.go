var extractionsCmd = &cobra.Command{
	Use:   "extractions",
	Short: "Extract structured JSON from documents using your defined schema",
	Long: `Run schema-driven JSON extractions over documents.

An extraction takes a document (uploaded blob, URL, or pre-built document
descriptor) plus a JSON schema and returns a JSON object conforming to the
schema. Results are persisted server-side and can be listed, fetched, and
introspected for per-field provenance.

Typical flow: ` + "`retab extractions create`" + ` to run a one-shot extraction,
` + "`retab extractions stream`" + ` when latency matters and you want progressive
output, ` + "`retab extractions sources`" + ` to inspect where each field came from.`,
}

func newExtractionRequest(cmd *cobra.Command) (retab.ExtractionsCreateParams, error) {
	metaPairs, _ := cmd.Flags().GetStringArray("metadata")
	metadata, err := parseKVStringList(metaPairs)
	if err != nil {
		return retab.ExtractionsCreateParams{}, err
	}
	model, err := requireNonBlankFlag(cmd, "model")
	if err != nil {
		return retab.ExtractionsCreateParams{}, err
	}
	nConsensus, _ := cmd.Flags().GetInt("n-consensus")
	instructions, _ := cmd.Flags().GetString("instructions")
	bustCache, _ := cmd.Flags().GetBool("bust-cache")
	schema, err := resolveSchema(cmd)
	if err != nil {
		return retab.ExtractionsCreateParams{}, err
	}
	messagesFile, _ := cmd.Flags().GetString("messages-file")
	var messages []map[string]interface{}
	if messagesFile != "" {
		arr, err := readJSONArray(messagesFile)
		if err != nil {
			return retab.ExtractionsCreateParams{}, fmt.Errorf("--messages-file: %w", err)
		}
		for _, m := range arr {
			obj, ok := m.(map[string]any)
			if !ok {
				return retab.ExtractionsCreateParams{}, fmt.Errorf("--messages-file: each item must be a JSON object")
			}
			messages = append(messages, obj)
		}
	}
	doc, err := resolveDocument(cmd)
	if err != nil {
		return retab.ExtractionsCreateParams{}, err
	}
	jsonSchema, ok := schema.(map[string]interface{})
	if !ok {
		return retab.ExtractionsCreateParams{}, fmt.Errorf("json schema must be a JSON object")
	}
	params := retab.ExtractionsCreateParams{
		Document:           doc,
		JSONSchema:         jsonSchema,
		Model:              ptr(model),
		Instructions:       ptr(instructions),
		Metadata:           &metadata,
		AdditionalMessages: messages,
		BustCache:          ptr(bustCache),
		Background:         primitiveBackgroundParam(cmd),
	}
	// Only send bounded int params when the user actually set them. The legacy
	// --image-resolution-dpi flag is accepted for compatibility but ignored.
	if cmd.Flags().Changed("n-consensus") {
		params.NConsensus = ptr(nConsensus)
	}
	return params, nil
}

var extractionsCreateCmd = &cobra.Command{
	Use:   "create",
	Short: "Create an extraction",
	Long: `Extract structured JSON from a document given a JSON schema.

The document source is one of ` + "`--file`" + ` (upload + extract in one call),
` + "`--url`" + ` (download by URL server-side), ` + "`--file-id`" + ` (reuse a
previously-uploaded blob — see ` + "`retab files upload`" + `), or
` + "`--document-file`" + ` (pre-built JSON document descriptor).

The schema comes from ` + "`--json-schema`" + ` (literal) or
` + "`--json-schema-file`" + ` (path, or ` + "`-`" + ` for stdin). Schemas can be
hand-written or generated by ` + "`retab schemas generate`" + `.

For streaming output (one event per line, useful on slow extractions), see
` + "`retab extractions stream`" + `.`,
	Example: `  # Upload and extract in one call
  retab extractions create \
    --file ./invoice.pdf \
    --json-schema-file ./invoice-schema.json \
    --model gpt-4o

  # Reuse an already-uploaded file
  retab extractions create \
    --file-id file_abc123 \
    --json-schema-file ./schema.json \
    --model gpt-4o

 # Multiple metadata key=value tags for audit
  retab extractions create \
    --file ./doc.pdf --json-schema-file ./s.json --model gpt-4o \
    --metadata customer=acme --metadata env=prod`,
	RunE: runE(func(cmd *cobra.Command, args []string) error {
		req, err := newExtractionRequest(cmd)
		if err != nil {
			return err
		}
		client, err := newClient(cmd)
		if err != nil {
			return err
		}
		ctx, cancel := ctxFor(cmd)
		defer cancel()
		result, err := client.Extractions.Create(ctx, &req)
		if err != nil {
			return err
		}
		return maybeWaitForPrimitiveCreate(cmd, extractionWaitSpec, result)
	}),
}

