var extractionsCmd = &cobra.Command{
	Use:   "extractions",
	Short: "Extract structured JSON from documents using your defined schema",
	Long: `Run schema-driven JSON extractions over documents.

An extraction takes a document (uploaded blob, URL, or pre-built document
descriptor) plus a JSON schema and returns a JSON object conforming to the
schema. Results are persisted server-side and can be listed, fetched, and
introspected for per-field provenance.

Typical flow: ` + "`retab extractions create`" + ` to run a one-shot extraction,
` + "`retab extractions stream`" + ` when latency matters and you want progressive
output, ` + "`retab extractions sources`" + ` to inspect where each field came from.`,
}

func newExtractionRequest(cmd *cobra.Command) (retab.ExtractionCreateRequest, error) {
	doc, err := resolveDocument(cmd)
	if err != nil {
		return retab.ExtractionCreateRequest{}, err
	}
	schema, err := resolveSchema(cmd)
	if err != nil {
		return retab.ExtractionCreateRequest{}, err
	}
	model, _ := cmd.Flags().GetString("model")
	imageDPI, _ := cmd.Flags().GetInt("image-resolution-dpi")
	nConsensus, _ := cmd.Flags().GetInt("n-consensus")
	instructions, _ := cmd.Flags().GetString("instructions")
	bustCache, _ := cmd.Flags().GetBool("bust-cache")
	metaPairs, _ := cmd.Flags().GetStringArray("metadata")
	metadata, err := parseKVStringList(metaPairs)
	if err != nil {
		return retab.ExtractionCreateRequest{}, err
	}
	messagesFile, _ := cmd.Flags().GetString("messages-file")
	var messages []retab.Resource
	if messagesFile != "" {
		arr, err := readJSONArray(messagesFile)
		if err != nil {
			return retab.ExtractionCreateRequest{}, fmt.Errorf("--messages-file: %w", err)
		}
		for _, m := range arr {
			obj, ok := m.(map[string]any)
			if !ok {
				return retab.ExtractionCreateRequest{}, fmt.Errorf("--messages-file: each item must be a JSON object")
			}
			messages = append(messages, retab.Resource(obj))
		}
	}
	return retab.ExtractionCreateRequest{
		Document:           doc,
		JSONSchema:         schema,
		Model:              model,
		ImageResolutionDPI: imageDPI,
		NConsensus:         nConsensus,
		Instructions:       instructions,
		Metadata:           metadata,
		AdditionalMessages: messages,
		BustCache:          bustCache,
	}, nil
}

var extractionsCreateCmd = &cobra.Command{
	Use:   "create",
	Short: "Create an extraction",
	Long: `Extract structured JSON from a document given a JSON schema.

The document source is one of ` + "`--file`" + ` (upload + extract in one call),
` + "`--url`" + ` (download by URL server-side), ` + "`--file-id`" + ` (reuse a
previously-uploaded blob — see ` + "`retab files upload`" + `), or
` + "`--document-file`" + ` (pre-built JSON document descriptor).

The schema comes from ` + "`--json-schema`" + ` (literal) or
` + "`--json-schema-file`" + ` (path, or ` + "`-`" + ` for stdin). Schemas can be
hand-written or generated by ` + "`retab schemas generate`" + `.

For streaming output (one event per line, useful on slow extractions), see
` + "`retab extractions stream`" + `.`,
	Example: `  # Upload and extract in one call
  retab extractions create \
    --file ./invoice.pdf \
    --json-schema-file ./invoice-schema.json \
    --model gpt-4o

  # Reuse an already-uploaded file
  retab extractions create \
    --file-id file_abc123 \
    --json-schema-file ./schema.json \
    --model gpt-4o

  # Multiple metadata key=value tags for audit
  retab extractions create \
    --file ./doc.pdf --json-schema-file ./s.json --model gpt-4o \
    --metadata customer=acme --metadata env=prod`,
	RunE: runE(func(cmd *cobra.Command, args []string) error {
		client, err := newClient(cmd)
		if err != nil {
			return err
		}
		ctx, cancel := ctxFor(cmd)
		defer cancel()
		req, err := newExtractionRequest(cmd)
		if err != nil {
			return err
		}
		result, err := client.Extractions.Create(ctx, req)
		if err != nil {
			return err
		}
		return printJSON(result)
	}),
}

var extractionsStreamCmd = &cobra.Command{
	Use:   "stream",
	Short: "Stream an extraction; emits one JSON object per line",
	Long: `Stream an extraction as newline-delimited JSON (NDJSON).

Each line on stdout is one complete JSON event describing the in-progress
extraction state — pipe through ` + "`jq`" + ` or any line-oriented tool. Prefer
this over ` + "`retab extractions create`" + ` when extractions run longer than a
few seconds and you want partial results visible as they land.

Flags and document/schema resolution are identical to
` + "`retab extractions create`" + `.`,
	Example: `  # Stream an extraction, line by line
  retab extractions stream \
    --file ./contract.pdf \
    --json-schema-file ./schema.json \
    --model gpt-4o

  # Pipe events through jq as they arrive
  retab extractions stream \
    --file-id file_abc123 --json-schema-file ./s.json --model gpt-4o \
    | jq -c 'select(.type == "delta")'`,
	RunE: runE(func(cmd *cobra.Command, args []string) error {
		client, err := newClient(cmd)
		if err != nil {
			return err
		}
		ctx, cancel := ctxFor(cmd)
		defer cancel()
		req, err := newExtractionRequest(cmd)
		if err != nil {
			return err
		}
		stream, err := client.Extractions.CreateStream(ctx, req)
		if err != nil {
			return err
		}
		defer stream.Close()
		for {
			item, err := stream.Next()
			if err != nil {
				if errors.Is(err, io.EOF) {
					return nil
				}
				return err
			}
			if err := printNDJSON(item); err != nil {
				return err
			}
		}
	}),
}

var extractionsListCmd = &cobra.Command{
	Use:   "list",
	Short: "List extractions",
	Long: `List extractions, newest first by default.

Results are paginated using cursors: pass ` + "`--after`" + ` with the last
extraction id from the previous page to fetch the next page, or ` + "`--before`" + `
to walk backwards. Filter by origin (the resource that produced the
extraction) with ` + "`--origin-type`" + ` and ` + "`--origin-id`" + `, or by
arbitrary tags set at create time with ` + "`--metadata`" + ` (repeatable).`,
	Example: `  # Most recent 25 extractions
  retab extractions list --limit 25

  # Filter by metadata tag set at create time
  retab extractions list --metadata customer=acme --metadata env=prod

  # Cursor-paginate from a known extraction id
  retab extractions list --after extr_xyz789 --limit 50`,
	RunE: runE(func(cmd *cobra.Command, args []string) error {
		client, err := newClient(cmd)
		if err != nil {
			return err
		}
		ctx, cancel := ctxFor(cmd)
		defer cancel()
		params := retab.ListExtractionsParams{ListParams: collectListParams(cmd)}
		params.OriginType, _ = cmd.Flags().GetString("origin-type")
		params.OriginID, _ = cmd.Flags().GetString("origin-id")
		metaPairs, _ := cmd.Flags().GetStringArray("metadata")
		md, err := parseKVStringList(metaPairs)
		if err != nil {
			return err
		}
		params.Metadata = md
		result, err := client.Extractions.List(ctx, &params)
		if err != nil {
			return err
		}
		return printResult(cmd, result)
	}),
}

